var miModulo = {
  sumar: function (valor1, valor2) {
    return valor1 + valor2;
  },
  restar: function (valor1, valor2) {
    return valor1 - valor2;
  },
  dividir: function (valor1, valor2) {
    return valor1 / valor2;
  },
  multiplicar: function (valor1, valor2) {
    return valor1 * valor2;
  },
};
module.exports = miModulo;
